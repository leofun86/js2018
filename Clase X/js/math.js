let val1 = -4;
let val2 = 4;

//abs: devuelve el valor absoluto de un número
document.write(Math.abs(val1)+'<br>');
//ceil: devuelve un número entero más pequeño, mayor o igual a un número
document.write(Math.ceil(val2)+'<br>');
//ceil: devuelve un número entero más grande, menor o igual a un número
document.write(Math.floor(val2)+'<br>');
//pow: devuelve un número elevado a una potencia
document.write(Math.pow(val2, 2)+'<br>');
//random: genera un número pseudoaleatorio entre 0 y 1
document.write(Math.random()+'<br>');
